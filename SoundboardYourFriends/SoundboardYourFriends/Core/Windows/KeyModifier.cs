namespace SoundboardYourFriends.Core.Windows
{
    public enum KeyModifier
    {
        // (NONE)
        MOD_NONE = 0x0000,

        // ALT
        MOD_ALT = 0x0001,

        // CTRL
        MOD_CONTROL = 0x0002,

        // SHIFT
        MOD_SHIFT = 0x0004,

        // WINDOWS
        MOD_WIN = 0x0008
    }
}